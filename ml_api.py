def load_colleges_from_csv():
    df = pd.read_csv('cet_official_data_with_coep.csv')
    
    COLLEGES_DATA = {}
    for _, row in df.iterrows():
        college_name = row['college_name']
        course_name = row['course_name']
        caste = row['caste_category']
        cutoff = row['cutoff_percentile']
        
        if college_name not in COLLEGES_DATA:
            COLLEGES_DATA[college_name] = {}
        if course_name not in COLLEGES_DATA[college_name]:
            COLLEGES_DATA[college_name][course_name] = {}
        
        COLLEGES_DATA[college_name][course_name][caste] = cutoff
    
    return COLLEGES_DATA

