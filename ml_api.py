class CollegeRecommender:
    def __init__(self, colleges_data, best_model, le_category, le_course, le_college, feature_columns):
        self.colleges_data = colleges_data
        self.model = best_model
        self.le_category = le_category
        self.le_course = le_course
        self.le_college = le_college
        self.feature_columns = feature_columns

    def recommend(self, student_percentile, preferred_course, category, top_n=10):
        """Get college recommendations for a student"""
        recommendations = []

        for college_name, college_data in self.colleges_data.items():
            if preferred_course in college_data:
                course_data = college_data[preferred_course]
                if category in course_data:
                    cutoff = course_data[category]

                    # Prepare features
                    try:
                        category_encoded = self.le_category.transform([category])[0]
                        course_encoded = self.le_course.transform([preferred_course])[0]
                        college_encoded = self.le_college.transform([college_name])[0]
                    except:
                        continue

                    # Calculate features
                    percentile_gap = student_percentile - cutoff
                    safety_margin = student_percentile - cutoff
                    relative_performance = student_percentile / cutoff if cutoff > 0 else 0

                    features = [student_percentile, cutoff, percentile_gap,
                               safety_margin, relative_performance,
                               category_encoded, course_encoded, college_encoded]

                    # Create DataFrame with feature names to avoid warnings
                    features_df = pd.DataFrame([features], columns=self.feature_columns)

                    # Predict probability
                    try:
                        probability = self.model.predict_proba(features_df)[0][1]

                        if probability > 0.3:  # Lower threshold for more recommendations
                            recommendations.append({
                                'college': college_name,
                                'course': preferred_course,
                                'category': category,
                                'cutoff': cutoff,
                                'student_percentile': student_percentile,
                                'admission_probability': round(probability, 4),
                                'safety_margin': round(safety_margin, 2),
                                'chance': 'High' if probability > 0.7 else 'Medium' if probability > 0.5 else 'Low'
                            })
                    except Exception as e:
                        print(f"Prediction error for {college_name}: {e}")
                        continue

        # Sort by probability
        recommendations.sort(key=lambda x: x['admission_probability'], reverse=True)
        return recommendations[:top_n]

