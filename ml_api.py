# ==================== DATA PREPARATION FUNCTIONS ====================
def create_training_dataset(colleges_data):
    """Convert JSON college data to training dataset"""
    training_examples = []

    for college_name, college_data in colleges_data.items():
        for course_name, course_data in college_data.items():
            if isinstance(course_data, dict):
                for category, cutoff in course_data.items():
                    if isinstance(cutoff, (int, float)) and cutoff > 0:
                        # Create multiple student scenarios (40% to 100%)
                        for student_percentile in range(40, 101, 2):
                            # Label: 1 if student can get admission (with 2% buffer)
                            label = 1 if student_percentile >= (cutoff - 2) else 0

                            training_examples.append({
                                'student_percentile': student_percentile,
                                'college_cutoff': cutoff,
                                'category': category,
                                'course': course_name,
                                'college': college_name,
                                'percentile_gap': student_percentile - cutoff,
                                'admission_possible': label
                            })

    return pd.DataFrame(training_examples)

def prepare_features(df):
    """Prepare features for model training"""
    # Encode categorical variables
    le_category = LabelEncoder()
    le_course = LabelEncoder()
    le_college = LabelEncoder()

    df['category_encoded'] = le_category.fit_transform(df['category'])
    df['course_encoded'] = le_course.fit_transform(df['course'])
    df['college_encoded'] = le_college.fit_transform(df['college'])

    # Create additional features
    df['safety_margin'] = df['student_percentile'] - df['college_cutoff']
    df['relative_performance'] = df['student_percentile'] / df['college_cutoff']

    # Feature columns
    feature_columns = ['student_percentile', 'college_cutoff', 'percentile_gap',
                      'safety_margin', 'relative_performance', 'category_encoded',
                      'course_encoded', 'college_encoded']

    return df[feature_columns], df['admission_possible'], le_category, le_course, le_college

